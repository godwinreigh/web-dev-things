
import turtle

a_turtle = turtle.Turtle()

def square():
    a_turtle.forward(100)
    a_turtle.right(90)
    a_turtle.forward(100)
    a_turtle.right(90)
    a_turtle.forward(100)
    a_turtle.right(90)
    a_turtle.forward(100)
    a_turtle.left(90)


square()

a_turtle.forward(100)

square()

a_turtle.right(90)
a_turtle.forward(100)

square()
a_turtle.right(90)

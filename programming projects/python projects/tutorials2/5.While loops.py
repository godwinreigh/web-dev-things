import turtle
cuteturtle = turtle.Turtle()

def square():
    cuteturtle.forward(100)
    cuteturtle.left(90)
    cuteturtle.forward(100)
    cuteturtle.left(90)
    cuteturtle.forward(100)
    cuteturtle.left(90)
    cuteturtle.forward(100)
    cuteturtle.left(90)

    sadturtle = 'sad'
    while sadturtle == 'sad':
        cuteturtle.forward(10)

square()

